print('*** Operadores de Comparacion (Relacionales)')
a, b = 7, 5

print(f'Valor inicial a: {a}')
print(f'Valor inicial b: {b}')

# Operador igualdad == . Regresa un valor logico (True/False)
resultado = a == b
print(f'Resultado de a == b es: {resultado}')

# Operador diferente !=
resultado = a != b
print(f'Resultado de a != b es: {resultado}')

# Operador mayor que >
resultado = a > b
print(f'Resultado de a > b es: {resultado}')

# Operador mayor o igual que >=
resultado = a >= b
print(f'Resultado de a >= b es: {resultado}')

# Operador menor que <
resultado = a < b
print(f'Resultado a < b es: {resultado}')

# Operador menor o igual que <=
resultado = a <= b
print(f'Resultado a <= b es: {resultado}')
