print('*** Operadores Aritmeticos ***')
a, b = 10, 5
print(f'Valor inicial a: {a}')
print(f'Valor inicial b: {b}')

# Operador suma +
resultado = a + b
print(f'Resultado Operador suma a + b es: {resultado}')

# Operador resta -
resultado = a - b
print(f'Resultado Operador suma a - b es: {resultado}')

# Operadpr multiplicacion *
resultado = a * b
print(f'Resultado Operador suma a * b es: {resultado}')

# Operador division /
resultado = a / b
print(f'Resultado Operador suma a / b es: {resultado}')

# Operador modulo/residuo %
resultado = a % b
print(f'Resultado Operador suma a % b es: {resultado}')

# Operador exponente ** (potencia)
resultado = 2 ** 3  # base 2:, exponente: 3 = 2*2*2 = 8
print(f'Resultado Operador suma a ** b es: {resultado}')
